# Falsy values - Są to takie wartości, które przy konwersji na boolean mają wartość false
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(()))
print(bool([]))
print(bool({}))
print(bool(""))
print(bool(None))

print(bool(True))
print(bool(10))
print(bool(-10))
print(bool(10.10))
print(bool(-10.10))
print(bool([0]))
print(bool({0}))

