def addNumber(a, b):
    return a + b

def subtractNumber(a, b):
    return a - b

def multiplyNumber(a, b):
    return a * b

def add4numbers(n1, n2, n3, n4):
    result = n1 + n2 + n3 + n4
    return result

print(addNumber(1, 2))
print(subtractNumber(1, 2))
print(multiplyNumber(1, 2))
print(add4numbers(1, 2, 3, 4))